<script type="text/javascript">
    function saveContact(){
        $("#loadMsg").html("");
        var hasError = 0;
        
        if (!$('#contact_name').val()) {
            $('#contact_name').css({"border-color": "red"});
            $('#contact_name').focus();
            return false;
        } else {
            $('#contact_name').css({"border-color": "#a9a9a9"});
        }
        
        if (!$('#contact_email').val()) {
            $('#contact_email').css({"border-color": "red"});
            $('#contact_email').focus();
            return false;
        } else {
            $('#contact_email').css({"border-color": "#a9a9a9"});
        }
        var datastring = $("#myform").serialize();
        if (hasError == 1) {
            return false;
        } else {
            $("#loadMsg").html("<img src='<?PHP echo base_url(); ?>images/ajax-loader.gif'/>");
            $.ajax({
                url: "<?php echo base_url(); ?>home/saveContactDetails",
                type: "POST",
                data: datastring,
                success: function (msg){
                    $('#contact_name').val("");
                    $('#contact_email').val("");
                    $('#message').val("");
                    $("#loadMsg").html('<div class="alert alert-success">'+msg+'</div>');
                }
            });
            return false;
        }
    }
</script>